package operadores;

public class OperadoresAtribuicao {

	public static void main(String[] args) {
		int x=1;
		x++;
		System.out.println(x);
		x--;
		System.out.println(x);
		x+=30;
		System.out.println(x);
		x-=25;
		System.out.println(x);
		x*=4;
		System.out.println(x);
		x/=2;
		System.out.println(x);
		x%=3;
		System.out.println(x);
		
	}

}
